#!/bin/bash

SOURCE_DB_HOST=$SOURCE_DB_HOST
SOURCE_USER=$SOURCE_USER
SOURCE_PASSWORD=$SOURCE_PASSWORD
SOURCE_DB_NAME=$SOURCE_DB_NAME

TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
DUMPFILE="/backup/${TIMESTAMP}_dump_${SOURCE_DB_NAME}_.sql.gz"

PGPASSWORD=$SOURCE_PASSWORD pg_dump -h $SOURCE_DB_HOST -U $SOURCE_USER -d $SOURCE_DB_NAME | gzip >  "$DUMP_FILE"
